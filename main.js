// Mobile Pinch, Pan and Double Tap zoom
function setupTouchZoom(img) {
  let scale = 1;
  let startDistance = 0;
  let lastTapTime = 0;
  
  img.style.transform = `scale(1) translate3d(0, 0, 0)`;

  // Double tap to toggle zoom
  img.addEventListener('touchend', (e) => {
    const currentTime = new Date().getTime();
    const tapLength = currentTime - lastTapTime;
    if (tapLength < 300 && tapLength > 0) {
      e.preventDefault();
      if (scale > 1) {
        scale = 1;
      } else {
        scale = 2;
      }
      img.style.transform = `scale(${scale}) translate3d(0,0,0)`;
      img.style.transition = 'transform 0.3s cubic-bezier(0.25, 1, 0.5, 1)';
    }
    lastTapTime = currentTime;
  });

  // Touch tracking for pinch gestures
  img.addEventListener('touchstart', (e) => {
    if (e.touches.length === 2) {
      startDistance = Math.hypot(
        e.touches[0].clientX - e.touches[1].clientX,
        e.touches[0].clientY - e.touches[1].clientY
      );
      img.style.transition = 'none';
    }
  });

  img.addEventListener('touchmove', (e) => {
    if (e.touches.length === 2 && startDistance > 0) {
      e.preventDefault();
      const currentDistance = Math.hypot(
        e.touches[0].clientX - e.touches[1].clientX,
        e.touches[0].clientY - e.touches[1].clientY
      );
      
      const factor = currentDistance / startDistance;
      scale = Math.min(Math.max(1, scale * factor), 4);
      img.style.transform = `scale(${scale}) translate3d(0,0,0)`;
      startDistance = currentDistance;
    }
  });

  img.addEventListener('touchend', (e) => {
    if (e.touches.length < 2) {
      startDistance = 0;
      if (scale < 1.05) {
        scale = 1;
        img.style.transform = `scale(1) translate3d(0,0,0)`;
        img.style.transition = 'transform 0.3s cubic-bezier(0.25, 1, 0.5, 1)';
      }
    }
  });
}

// Bind all interaction events
function setupGalleryListeners() {
  document.getElementById('folderGalBackdrop').addEventListener('click', closeGalleryModal);
  document.getElementById('folderGalClose').addEventListener('click', closeGalleryModal);

  document.getElementById('folderGalBtnArc').addEventListener('click', () => setGalleryView('arc'));
  document.getElementById('folderGalBtnGrid').addEventListener('click', () => setGalleryView('grid'));

  document.getElementById('folderGalPrev').addEventListener('click', () => navigateArc('prev'));
  document.getElementById('folderGalNext').addEventListener('click', () => navigateArc('next'));

  document.getElementById('lightboxClose').addEventListener('click', closeLightbox);
  document.getElementById('imageLightbox').addEventListener('click', (e) => {
    if (e.target.id === 'imageLightbox') closeLightbox();
  });

  document.getElementById('lightboxPrev').addEventListener('click', () => navigateLightbox('prev'));
  document.getElementById('lightboxNext').addEventListener('click', () => navigateLightbox('next'));

  // Keyboard navigation shortcuts
  document.addEventListener('keydown', (e) => {
    const modal = document.getElementById('folderGalleryModal');
    const lightbox = document.getElementById('imageLightbox');

    if (!modal.classList.contains('active')) return;

    if (e.key === 'Escape') {
      if (lightbox.classList.contains('active')) {
        closeLightbox();
      } else {
        closeGalleryModal();
      }
    }

    if (lightbox.classList.contains('active')) {
      if (e.key === 'ArrowRight') navigateLightbox('next');
      if (e.key === 'ArrowLeft') navigateLightbox('prev');
    } else {
      if (e.key === 'ArrowRight') navigateArc('next');
      if (e.key === 'ArrowLeft') navigateArc('prev');
    }
  });

  // Mouse wheel scroll triggers radial shift
  const track = document.getElementById('folderGalArcTrack');
  track.addEventListener('wheel', (e) => {
    e.preventDefault();
    if (e.deltaY > 0) {
      navigateArc('next');
    } else {
      navigateArc('prev');
    }
  }, { passive: false });

  // Mobile swipes trigger radial shift
  let touchStartX = 0;
  track.addEventListener('touchstart', (e) => {
    touchStartX = e.touches[0].clientX;
  }, { passive: true });

  track.addEventListener('touchend', (e) => {
    const diff = e.changedTouches[0].clientX - touchStartX;
    if (Math.abs(diff) > 40) {
      if (diff < 0) {
        navigateArc('next');
      } else {
        navigateArc('prev');
      }
    }
  }, { passive: true });
}
