/**
 * 3D Glassmorphic Tilt Effect
 */
function setupFolderTilt() {
  const folderWrapper = document.getElementById('folderWrapper');
  const folderCard = document.getElementById('folderCard');

  if (!folderWrapper || !folderCard) return;

  folderWrapper.addEventListener('mousemove', (e) => {
    const rect = folderWrapper.getBoundingClientRect();
    const x = e.clientX - rect.left - rect.width / 2;
    const y = e.clientY - rect.top - rect.height / 2;
    
    const maxTilt = 8;
    const degX = (y / (rect.height / 2)) * -maxTilt;
    const degY = (x / (rect.width / 2)) * maxTilt;
    
    folderCard.style.animation = 'none';
    folderCard.style.transform = `rotateX(${degX}deg) rotateY(${degY}deg) scale3d(1.02, 1.02, 1.02)`;
  });

  folderWrapper.addEventListener('mouseleave', () => {
    folderCard.style.transform = 'rotateX(0deg) rotateY(0deg) scale3d(1, 1, 1)';
    folderCard.style.transition = 'transform 0.5s cubic-bezier(0.25, 1, 0.5, 1)';
    
    setTimeout(() => {
      folderCard.style.animation = 'floatFolder 8s ease-in-out infinite';
      folderCard.style.transition = 'transform 0.5s cubic-bezier(0.25, 1, 0.5, 1)';
    }, 500);
  });
}

/**
 * Dynamic Accent Color Swatch System
 */
function setupColorSwatches() {
  const swatches = document.querySelectorAll('.swatch');
  const root = document.documentElement;
  const bgGlow = document.getElementById('bgGlow');

  if (!swatches.length || !root || !bgGlow) return;

  swatches.forEach(swatch => {
    swatch.addEventListener('click', () => {
      swatches.forEach(s => s.classList.remove('active'));
      swatch.classList.add('active');

      const color = swatch.getAttribute('data-color');
      root.style.setProperty('--lime', color);

      if (swatch.classList.contains('swatch-black')) {
        const defaultLime = '#A8E600';
        root.style.setProperty('--lime', defaultLime);
        bgGlow.style.background = 'radial-gradient(circle, rgba(245, 242, 234, 0.22) 0%, rgba(168, 230, 0, 0.04) 40%, rgba(10, 10, 10, 0) 70%)';
      } else if (swatch.classList.contains('swatch-orange')) {
        bgGlow.style.background = 'radial-gradient(circle, rgba(255, 138, 61, 0.25) 0%, rgba(255, 138, 61, 0.05) 45%, rgba(10, 10, 10, 0) 70%)';
      } else if (swatch.classList.contains('swatch-lime')) {
        bgGlow.style.background = 'radial-gradient(circle, rgba(168, 230, 0, 0.25) 0%, rgba(26, 61, 15, 0.05) 45%, rgba(10, 10, 10, 0) 70%)';
      } else if (swatch.classList.contains('swatch-white')) {
        bgGlow.style.background = 'radial-gradient(circle, rgba(245, 242, 234, 0.3) 0%, rgba(245, 242, 234, 0.08) 45%, rgba(10, 10, 10, 0) 70%)';
      }

      createClickRipple(swatch);
    });
  });
}

