/**
 * Creates tactile ripple on element click
 */
function createClickRipple(element) {
  const ripple = document.createElement('span');
  ripple.style.position = 'absolute';
  ripple.style.width = '100%';
  ripple.style.height = '100%';
  ripple.style.borderRadius = '50%';
  ripple.style.border = '2px solid var(--lime)';
  ripple.style.top = '0';
  ripple.style.left = '0';
  ripple.style.opacity = '1';
  ripple.style.transform = 'scale(1)';
  ripple.style.transition = 'transform 0.4s ease-out, opacity 0.4s ease-out';
  ripple.style.pointerEvents = 'none';

  element.appendChild(ripple);

  requestAnimationFrame(() => {
    ripple.style.transform = 'scale(2)';
    ripple.style.opacity = '0';
  });

  setTimeout(() => {
    ripple.remove();
  }, 400);
}

/**
 * Scroll Reveal for Portfolio Cards
 */
function setupWorksReveal() {
  const cards = document.querySelectorAll('.work-card');
  if (!cards.length) return;

  cards.forEach(card => {
    card.style.opacity = '0';
    card.style.transform = 'translateY(24px)';
    card.style.transition = 'opacity 0.6s cubic-bezier(0.25, 1, 0.5, 1), transform 0.6s cubic-bezier(0.25, 1, 0.5, 1)';
  });

  const observer = new IntersectionObserver((entries) => {
    entries.forEach((entry, i) => {
      if (entry.isIntersecting) {
        setTimeout(() => {
          entry.target.style.opacity = '1';
          entry.target.style.transform = 'translateY(0)';
        }, i * 80);
        observer.unobserve(entry.target);
      }
    });
  }, { threshold: 0.15 });

  cards.forEach(card => observer.observe(card));
}

