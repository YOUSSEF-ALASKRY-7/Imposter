// Toggle between Arc View and Grid View
function setGalleryView(view) {
  const modal        = document.getElementById('folderGalleryModal');
  const btnArc       = document.getElementById('folderGalBtnArc');
  const btnGrid      = document.getElementById('folderGalBtnGrid');
  const arcContainer = document.getElementById('folderGalArcContainer');
  const gridContainer= document.getElementById('folderGalGridContainer');
  const prevBtn      = document.getElementById('folderGalPrev');
  const nextBtn      = document.getElementById('folderGalNext');
  const counter      = document.getElementById('folderGalCounter');

  if (view === 'arc') {
    modal.classList.remove('view-grid');
    modal.classList.add('view-arc');

    arcContainer.classList.remove('hidden');
    gridContainer.classList.add('hidden');

    prevBtn.style.display  = '';
    nextBtn.style.display  = '';
    counter.style.display  = '';

    btnArc.classList.add('active');
    btnGrid.classList.remove('active');
    buildArcView(); // re-orient coordinates
  } else {
    modal.classList.remove('view-arc');
    modal.classList.add('view-grid');

    arcContainer.classList.add('hidden');
    gridContainer.classList.remove('hidden');

    prevBtn.style.display  = 'none';
    nextBtn.style.display  = 'none';
    counter.style.display  = 'none';

    btnGrid.classList.add('active');
    btnArc.classList.remove('active');
  }
}

// Dynamic Semicircle Angles based on visible count
function getArcAngles(count) {
  const centerDeg = -90;
  if (count <= 1) return [centerDeg];
  
  let span = 140; 
  if (count === 2) span = 40;
  else if (count === 3) span = 70;
  else if (count === 4) span = 90;
  else if (count === 5) span = 110;
  else if (count === 6) span = 125;
  
  const startDeg = centerDeg - span / 2;
  const step = span / (count - 1);
  const angles = [];
  for (let i = 0; i < count; i++) {
    angles.push(startDeg + step * i);
  }
  return angles;
}

// Build radial arch layout
function buildArcView() {
  const track = document.getElementById('folderGalArcTrack');
  const counter = document.getElementById('folderGalCounter');
  track.innerHTML = '';

  if (currentCategoryItems.length === 0) {
    counter.textContent = '0 / 0';
    return;
  }

  counter.textContent = `${currentArcIndex + 1} / ${currentCategoryItems.length}`;

  const total = currentCategoryItems.length;
  const showSlotsCount = Math.min(VISIBLE_ARC_SLOTS, total);
  const angles = getArcAngles(showSlotsCount);
  
  const radius = Math.min(window.innerWidth * 0.38, 300);
  const half = Math.floor(showSlotsCount / 2);

  for (let s = 0; s < showSlotsCount; s++) {
    let itemIndex;
    let isCentre = false;

    if (total >= VISIBLE_ARC_SLOTS) {
      itemIndex = ((currentArcIndex - half + s) + total) % total;
      isCentre = (s === half);
    } else {
      itemIndex = s;
      isCentre = (s === currentArcIndex);
    }

    const item = currentCategoryItems[itemIndex];
    const angleRad = angles[s] * (Math.PI / 180);
    const x = Math.cos(angleRad) * radius;
    const y = Math.sin(angleRad) * radius;
    const rot = angles[s] + 90;

    const card = document.createElement('div');
    card.className = 'folder-gal-card' + (isCentre ? ' centre' : '');
    card.style.transform = `translate3d(${x}px, ${y}px, 0) rotate(${rot}deg)`;
    card.style.zIndex = isCentre ? 100 : (10 - Math.abs(s - half));

    if (item.type === 'video') {
      card.innerHTML = `
        <video src="${item.src}" muted loop playsinline preload="metadata"></video>
        <div class="folder-gal-play-icon">▶</div>
      `;
      const video = card.querySelector('video');
      card.addEventListener('mouseenter', () => video.play());
      card.addEventListener('mouseleave', () => {
        video.pause();
        video.currentTime = 0;
      });
    } else if (item.type === 'pdf') {
      card.innerHTML = `
        <div class="folder-gal-pdf-icon">
          <svg class="folder-gal-pdf-svg" viewBox="0 0 24 24" fill="currentColor">
            <path d="M20 2H8c-1.1 0-2 .9-2 2v12c0 1.1.9 2 2 2h12c1.1 0 2-.9 2-2V4c0-1.1-.9-2-2-2zm-8.5 7.5c0 .83-.67 1.5-1.5 1.5H9v2H7.5V7H10c.83 0 1.5.67 1.5 1.5v1zm5 2c0 .83-.67 1.5-1.5 1.5h-2.5V7H15c.83 0 1.5.67 1.5 1.5v3zm4-2.5H19v1h1.5V11H19v2h-1.5V7h3v1.5zM9 8.5H10V10H9V8.5zm5.5 0h1v3h-1v-3zM4 6H2v14c0 1.1.9 2 2 2h14v-2H4V6z"/>
          </svg>
          <div class="folder-gal-pdf-title">${item.title}</div>
        </div>
      `;
    } else {
      const img = document.createElement('img');
      img.src = item.src;
      img.alt = item.title;
      img.loading = 'lazy';
      card.appendChild(img);
    }

    card.addEventListener('click', () => {
      if (isCentre) {
        openLightbox(itemIndex);
      } else {
        if (total >= VISIBLE_ARC_SLOTS) {
          currentArcIndex = itemIndex;
        } else {
          currentArcIndex = s;
        }
        buildArcView();
      }
    });

    track.appendChild(card);
  }
}

